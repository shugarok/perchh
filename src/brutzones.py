from PIL import Image
import outils


def zonesDrw(coordPoly : list[list], tImage  : int, img):


    #100 random colors

    colors = [
    (0, 0, 0), (0, 0, 64), (0, 0, 128), (0, 0, 192),
    (0, 51, 0), (0, 51, 64), (0, 51, 128), (0, 51, 192),
    (0, 102, 0), (0, 102, 64), (0, 102, 128), (0, 102, 192),
    (0, 153, 0), (0, 153, 64), (0, 153, 128), (0, 153, 192),
    (0, 204, 0), (0, 204, 64), (0, 204, 128), (0, 204, 192),
    (51, 0, 0), (51, 0, 64), (51, 0, 128), (51, 0, 192),
    (51, 51, 0), (51, 51, 64), (51, 51, 128), (51, 51, 192),
    (51, 102, 0), (51, 102, 64), (51, 102, 128), (51, 102, 192),
    (51, 153, 0), (51, 153, 64), (51, 153, 128), (51, 153, 192),
    (51, 204, 0), (51, 204, 64), (51, 204, 128), (51, 204, 192),
    (102, 0, 0), (102, 0, 64), (102, 0, 128), (102, 0, 192),
    (102, 51, 0), (102, 51, 64), (102, 51, 128), (102, 51, 192),
    (102, 102, 0), (102, 102, 64), (102, 102, 128), (102, 102, 192),
    (102, 153, 0), (102, 153, 64), (102, 153, 128), (102, 153, 192),
    (102, 204, 0), (102, 204, 64), (102, 204, 128), (102, 204, 192),
    (153, 0, 0), (153, 0, 64), (153, 0, 128), (153, 0, 192),
    (153, 51, 0), (153, 51, 64), (153, 51, 128), (153, 51, 192),
    (153, 102, 0), (153, 102, 64), (153, 102, 128), (153, 102, 192),
    (153, 153, 0), (153, 153, 64), (153, 153, 128), (153, 153, 192),
    (153, 204, 0), (153, 204, 64), (153, 204, 128), (153, 204, 192),
    (204, 0, 0), (204, 0, 64), (204, 0, 128), (204, 0, 192),
    (204, 51, 0), (204, 51, 64), (204, 51, 128), (204, 51, 192),
    (204, 102, 0), (204, 102, 64), (204, 102, 128), (204, 102, 192),
    (204, 153, 0), (204, 153, 64), (204, 153, 128), (204, 153, 192),
    (204, 204, 0), (204, 204, 64), (204, 204, 128), (204, 204, 192)]


    for i in range(len(coordPoly)):
        coordPoly[i][0] = coordPoly[i][0] * 5 + tImage // 2 + 0.001
        coordPoly[i][1] = tImage // 2 - coordPoly[i][1] * 5 + 0.001

    for x in range(0, tImage):
        for y in range(0, tImage):
            perch = [x, y]
            
            deplacements = outils.repeat_mvt(100000000, perch, coordPoly)

            sommetPP = outils.calc_dist_min(deplacements[-1], coordPoly)

            for i in range(0, len(coordPoly)):
                if sommetPP[0] == coordPoly[i]:
                    img.putpixel((perch[0], perch[1]), colors[i])

    for i in range(len(coordPoly)):
        x = int(coordPoly[i][0])
        y = int(coordPoly[i][1])

        img.putpixel((x, y), (0,0,0))


def mainbrutZone(coordPoly : list[list], tImage  : int):

    img = Image.new('RGB', (tImage, tImage), (255, 255, 255))


    zonesDrw(coordPoly, tImage, img)
    img.show()